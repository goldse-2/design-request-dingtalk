export function createProgramRpaParams({ task, productName, title, subtitle, otherText, referenceUrl, productUrls, sizeRequirement }) {
    const noProductImage = task?.noProductImage === true;
    const params = {
        "产品名称": noProductImage ? (task.analyzePrompt || NO_PRODUCT_ANALYZE_PROMPT) : (productName || '-'),
        "标题": title || '-',
        "副标题": subtitle || '-',
        "其他文案": otherText || '-',
        "竞品参考图链接": referenceUrl || '-',
        "任务ID": task?.id || '',
        "尺寸要求": sizeRequirement || '2K 自动识别'
    };

    if (!noProductImage) {
        params["白底参考图链接一"] = productUrls?.[0]?.url || '-';
        params["白底参考图链接二"] = productUrls?.[1]?.url || '-';
    }
    return params;
}
